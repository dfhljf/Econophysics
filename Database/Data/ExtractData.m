function [ parameters, market, agents ] = ExtractData( expId )
%extractdata 提取某个实验的数据
parameters=struct('Id',[],...
    'MaxStock',[],...
    'PeriodOfUpdateDividend',[],...
    'TradeFee',[],...
    'Count',[],...
    'Leverage',[],...
    'Lambda',[],...
    'P01',[],...
    'P10',[],...
    'PDividend',[],...
    'P',[],...
    'TransP',[],...
    'TimeWindow',[],...
    'PeriodOfTurn',[],...
    'MaxTurn',[],...
    'StartTime',[],...
    'Comments',[]);
[parameters.Id,...
    parameters.MaxStock,...
    parameters.PeriodOfUpdateDividend,...
    parameters.TradeFee,...
    parameters.Count,...
    parameters.Leverage,...
    parameters.Lambda,...
    parameters.P01,...
    parameters.P10,...
    parameters.PDividend,...
    parameters.P,...
    parameters.TransP,...
    parameters.TimeWindow,...
    parameters.PeriodOfTurn,...
    parameters.MaxTurn,...
    parameters.StartTime,...
    parameters.Comments]=mysql(['select * from parameters where Id=' num2str(expId)]);
[c1,c2,c3,c4,c5,c6,c7,c8]=mysql(['select Turn,Price,State,Returns,NumberOfPeople,NumberOfAndroids,AverageEndowment,Volume from market where ExperimentId=' num2str(expId)]);
market=[c1,c2,c3,c4,c5,c6,c7,c8];
[t1,t2,t3,t4,t5,t6,t7,t8]=mysql(['select Turn,Id,Cash,Stocks,Endowment,Dividend,TradeStocks,OrderNum from agents where ExperimentId=' num2str(expId)]);
agents=[t1,t2,t3,t4,t5,t6,t7,t8];
end

